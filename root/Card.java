	private static void solve(FastScanner s1, FastWriter out){

		char trump = s1.next().charAt(0);
		String c1 = s1.next();
		String c2 = s1.next();
		char cards[] = {'6', '7', '8', '9', 'T', 'J', 'Q', 'K' , 'A'};
		ArrayList<Character> arl = new ArrayList<>();
		for(char c:cards)
			arl.add(c);
		if(c1.indexOf(trump) >= 0 && c2.indexOf(trump) < 0)
		{
			out.print("YES");
			return;
		}
		if(c2.indexOf(trump) >= 0 && c1.indexOf(trump) < 0)
		{
			out.print("NO");
			return;
		}
		
		if(c1.charAt(1) == c2.charAt(1))
		{
			char ch1 = c1.charAt(0);
			char ch2 = c2.charAt(0);
			if(arl.indexOf(ch1) > arl.indexOf(ch2))
				out.print("YES");
			else
				out.print("NO");
		}
		else
			out.print("NO");


	}

