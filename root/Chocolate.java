	private static void solve(FastScanner s1, FastWriter out){

		int len = s1.nextInt();
		StringBuilder sb = new StringBuilder();
		for(int i=1;i<=len;i++)
		{
			int in = s1.nextInt();
			sb.append(in);
		}
		int ptr1 = 0;
		while(ptr1<len && sb.charAt(ptr1)=='0')ptr1++;
		int ptr2 = len-1;
		while(ptr2>=0 && sb.charAt(ptr2)=='0')ptr2--;
		
		if(ptr1==len)
		{
			out.print("0");
			return;
		}
		else
		{
			String clean = sb.substring(ptr1, ptr2+1);
			//out.print(clean);
			long ans = 1;
			int ct = 0;
			for(int i=0,l=clean.length();i<l;i++)
			{
				char ch = clean.charAt(i);
				if(ch == '1')
				{
					ans *= (long)(ct+1);
					ct = 0;
				}
				else
					ct++;
			}
			out.print(ans);
		}

	}

