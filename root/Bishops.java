    private static <Key> void frequency(Map<Key , java.lang.Long> mp , Key k)
    {
    	//Finds frequency of of each element of generic type Key
    	Long query = mp.get(k);
    	if(query == null)
    		mp.put(k, new Long(1));
    	else
    	{
    		mp.put(k, query + 1L);
    	}
    }
	private static void solve(FastScanner s1, FastWriter out){

		HashMap<Integer,Long> mp1 = new HashMap<>();
		HashMap<Integer,Long> mp2 = new HashMap<>();
		int n = s1.nextInt();
		while(n-->0)
		{
			int x = s1.nextInt();
			int y = s1.nextInt();
			frequency(mp1, (x+y));
			frequency(mp2, (x-y));
		}
		long total = 0;
		for (Map.Entry<Integer,Long> e : mp1.entrySet()) 
		{
			long curr = e.getValue();
			if(curr >= 2)
			{
				total += ((curr * (curr-1))/2);
			}
		}
		for (Map.Entry<Integer,Long> e : mp2.entrySet()) 
		{
			long curr = e.getValue();
			if(curr >= 2)
			{
				total += ((curr * (curr-1))/2);
			}
		}
		out.print(total);
	}

